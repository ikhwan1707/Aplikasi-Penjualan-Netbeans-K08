    private void cSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cSaveActionPerformed
        // TODO add your handling code here:

        if (cKodeJenis.getSelectedIndex() == 0 || "".equals(cNamaBarang.getText()) || "".equals(cHargaJual.getText())
                || "".equals(cHargaNet.getText()) || "".equals(cStok.getText())) {
            JOptionPane.showMessageDialog(null, "BOYY DATA WAJIB DIISI SEMUAH YAA !!!", "PESAN KESALAHAN", JOptionPane.ERROR_MESSAGE);
        } else {
            barangController.store(cNamaBarang.getText(), cKodeJenis.getSelectedItem().toString(), cHargaNet.getText(), cHargaJual.getText(), cStok.getText());
            pn_utama.removeAll();
            pn_utama.add(pn_main);
            pn_utama.repaint();
            pn_utama.revalidate();
            cKodeBarang.setText(barangController.autoIncrement());
            cNamaBarang.setText("");
            cKodeJenis.setSelectedIndex(0);
            cHargaNet.setText("");
            cHargaJual.setText("");
            cStok.setText("");
            index();
        }
    }//GEN-LAST:event_cSaveActionPerformed

