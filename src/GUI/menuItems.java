    public void index() {
        cKodeBarang.setText(barangController.autoIncrement());
        view.getDataVector().removeAllElements();
        view.fireTableDataChanged();

        if (tb_barang.getColumnCount() == 0) {
            tb_barang.setModel(view);
            view.addColumn("Kode Barang");
            view.addColumn("Nama Barang");
            view.addColumn("Nama Jenis");
            view.addColumn("Harga Net");
            view.addColumn("Harga Jual");
            view.addColumn("Stok");
        }

        for (String[] item : barangController.index()) {
            view.addRow(new Object[]{
                item[0],
                item[1],
                item[2],
                item[3],
                item[4],
                item[5]
            });
        }
    }

    public void show(String kodeBarang) {
        cKodeBarang.setText(barangController.autoIncrement());
        view.getDataVector().removeAllElements();
        view.fireTableDataChanged();

        if (tb_barang.getColumnCount() == 0) {
            tb_barang.setModel(view);
            view.addColumn("Kode Barang");
            view.addColumn("Nama Barang");
            view.addColumn("Nama Jenis");
            view.addColumn("Harga Net");
            view.addColumn("Harga Jual");
            view.addColumn("Stok");
        }

        String[] item = barangController.show(kodeBarang);
        view.addRow(new Object[]{
            item[0],
            item[1],
            item[2],
            item[3],
            item[4],
            item[5]
        });
    }

    public void loadCB() {
        for (String[] v : jenisController.index()) {
            cKodeJenis.addItem(v[0]);
            eKodeJenis.addItem(v[0]);
        }
    }

