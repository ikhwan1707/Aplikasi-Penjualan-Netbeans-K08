    private void EditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EditActionPerformed
        // TODO add your handling code here:
        if ("".equals(IndexKodeBarang.getText())) {
            JOptionPane.showMessageDialog(null, "Tidak dapat menemukan Kode Barang");
        } else {
            String[] v = barangController.show(IndexKodeBarang.getText());
            eKodeBarang.setText(v[0]);
            eNamaBarang.setText(v[1]);
            eKodeJenis.setSelectedItem(v[2]);
            eHargaNet.setText(v[4]);
            eHargaJual.setText(v[5]);
            eStok.setText(v[6]);
            pn_utama.removeAll();
            pn_utama.add(pn_add1);
            pn_utama.repaint();
            pn_utama.revalidate();
        }
    }//GEN-LAST:event_EditActionPerformed

