    private void loadData() {
        view.getDataVector().removeAllElements();
        view.fireTableDataChanged();

        if (tableData.getColumnCount() == 0) {
            tableData.setModel(view);
            view.addColumn("Kode");
            view.addColumn("Nama");
            view.addColumn("Alamat");
            view.addColumn("Kota Asal");
            view.addColumn("Email");
            view.addColumn("Telpon");
        }

        for (String[] v : d.index()) {
            view.addRow(new Object[]{
                v[0],
                v[1],
                v[2],
                v[3],
                v[4],
                v[5]
            });
        }
    }

