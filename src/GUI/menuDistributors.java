    private void cKodeKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_cKodeKeyTyped
        // TODO add your handling code here:
        char c = evt.getKeyChar();
        if (!(Character.isDigit(c) || (c == KeyEvent.VK_BACK_SPACE) || (c == KeyEvent.VK_DELETE))) {
            evt.consume();
            JOptionPane.showMessageDialog(null, "Masukan angka bang bukan huruf", "pesan kesalahan", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_cKodeKeyTyped

    private void cTelponKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_cTelponKeyTyped
        // TODO add your handling code here:
        char c = evt.getKeyChar();
        String Text = cTelpon.getText();

        if (Text.length() > 15) {
            evt.consume();
            JOptionPane.showMessageDialog(null, "cuma 15 angka bang gaboleh lebih !!!", "pesan kesalahan", JOptionPane.ERROR_MESSAGE);
        }
        if (!(Character.isDigit(c) || (c == KeyEvent.VK_BACK_SPACE) || (c == KeyEvent.VK_DELETE))) {
            evt.consume();
            JOptionPane.showMessageDialog(null, "Masukan angka bang bukan huruf", "pesan kesalahan", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_cTelponKeyTyped

