    private void index() {
        createIdPetugas.setText(p.autoIncrement());
        view.getDataVector().removeAllElements();
        view.fireTableDataChanged();

        if (tb_petugas.getColumnCount() == 0) {
            tb_petugas.setModel(view);
            view.addColumn("Id Petugas");
            view.addColumn("Nama Petugas");
            view.addColumn("Alamat");
            view.addColumn("Email");
            view.addColumn("Nomer Telpon");
        }

        List<String[]> data = p.index();
        for (String[] item : data) {
            view.addRow(new Object[]{
                item[0],
                item[1],
                item[2],
                item[3],
                item[4]
            });
        }
    }

    private void show(String kodePetugas) {
        createIdPetugas.setText(p.autoIncrement());
        view.getDataVector().removeAllElements();
        view.fireTableDataChanged();

        if (tb_petugas.getColumnCount() == 0) {
            tb_petugas.setModel(view);
            view.addColumn("Id Petugas");
            view.addColumn("Nama Petugas");
            view.addColumn("Alamat");
            view.addColumn("Email");
            view.addColumn("Nomer Telpon");
        }

        String[] item = p.show(kodePetugas);
        view.addRow(new Object[]{
            item[0],
            item[1],
            item[2],
            item[3],
            item[4]
        });
    }

