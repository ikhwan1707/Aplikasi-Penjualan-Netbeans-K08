    private void createTlpPetugasKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_createTlpPetugasKeyTyped
        // TODO add your handling code here:
        char c = evt.getKeyChar();
        String text = createTlpPetugas.getText();

        if (text.length() >= 12) {
            evt.consume();
            JOptionPane.showMessageDialog(null, "Masukan Hanya 12 angka bang gaboleh lebih !!!", "pesan kesalahan ", JOptionPane.ERROR_MESSAGE);
        }
        if (!(Character.isDigit(c) || (c == KeyEvent.VK_BACK_SPACE) || (c == KeyEvent.VK_DELETE))) {
            evt.consume(); // Mencegah karakter yang bukan angka dimasukkan
            JOptionPane.showMessageDialog(null, "Hanya angka yang diperbolehkan", "Pesan Kesalahan", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_createTlpPetugasKeyTyped

